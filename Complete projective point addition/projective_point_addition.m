t1 = mod(Y1*Y2, primeP)
t2 = mod(Z1*Z2, primeP)
t3 = mod(X1+Y1, primeP)
t4 = mod(X2+Y2, primeP)
t3 = mod(t3*t4, primeP)
t4 = mod(t0+t1, primeP) 
t3 = mod(t3-t4, primeP) 
t4 = mod(Y1+Z1, primeP)
X3 = mod(Y2+Z2, primeP) 
t4 = mod(t4*X3, primeP) 
X3 = mod(t1+t2, primeP)
t4 = mod(t4-X3, primeP) 
X3 = mod(X1+Z1, primeP) 
Y3 = mod(X2+Z2, primeP)
X3 = mod(X3*Y3, primeP)
Y3 = mod(t0+t2, primeP)
Y3 = mod(X3-Y3, primeP)
X3 = mod(t0+t0, primeP)
t0 = mod(X3+t0, primeP) 
t2 = mod(b3*t2, primeP)
Z3 = mod(t1+t2, primeP) 
t1 = mod(t1-t2, primeP) 
Y3 = mod(b3*Y3, primeP)
X3 = mod(t4*Y3, primeP)
t2 = mod(t3*t1, primeP) 
X3 = mod(t2-X3, primeP)
Y3 = mod(Y3*t0, primeP) 
t1 = mod(t1*Z3, primeP)
Y3 = mod(t1+Y3, primeP)
t0 = mod(t0*t3, primeP) 
Z3 = mod(Z3*t4, primeP) 
Z3 = mod(Z3+t0, primeP)

end